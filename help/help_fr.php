	<li><b class="ui-state-default">[CTRL] [A]</b> : panneau d'administration</li>
	<li><b class="ui-state-default">[CTRL] [N]</b> : gestion des news</li>
	<li><b class="ui-state-default">[CTRL] [U]</b> : gestion des utilisateurs</li>
	<li><b class="ui-state-default">[CTRL] [P]</b> : gestion des projets</li>
	<br />
	<b>Raccourcis développeurs :</b>
	<li><b class="ui-state-default">[C]</b> : ouvre le panneau de config</li>
	<li><b class="ui-state-default">[X]</b> : ouvre le panneau debug</li>
