	<li><b class="ui-state-default">[A]</b> : ouvre la partie "Assets" du projet actuel</li>
	<li><b class="ui-state-default">[B]</b> : ouvre le département "Final" du projet actuel</li>
	<li><b class="ui-state-default">[F1]</b> : ouvre l'aide dans un nouvel onglet</li>
	<li><b class="ui-state-default">[F4]</b> : retour sur la page d'accueil</li>
	<li><b class="ui-state-default">[F5]</b> : rafraîchi la totalité de la page</li>
	<li><b class="ui-state-default">[F11]</b> : affiche en plein écran</li>
