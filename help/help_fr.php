	<li><b class="ui-state-default">[P]</b> : ouvre les préférences utilisateur</li>
	<li><b class="ui-state-default">[T]</b> : ouvre le gestionnaire de TAGS</li>
	<li><b class="ui-state-default">[O]</b> : ouvre les NOTES personnelles</li>
	<li><b class="ui-state-default">[Z]</b> : retourne sur le dernier projet visité</li>
