<h2 class="ui-state-default ui-corner-top pad3">Shortcuts</h2>
<ul id="help_raccourcis" style="margin: 12px; padding: 0;">
	<b>Common shortcuts:</b>
	<li><b class="ui-state-default">[CTRL] [<span class="inline top ui-icon ui-icon-arrowthick-1-e"></span>]</b> : open / close right panel menu</li>
	<li><b class="ui-state-default">[CTRL] [<span class="inline top ui-icon ui-icon-arrowthick-1-w"></span>]</b> : open / close left panel menu</li>
	<li><b class="ui-state-default">[CTRL] [<span class="inline top ui-icon ui-icon-arrowthick-1-s"></span>]</b> : open / close messages panel</li>
	<li><b class="ui-state-default">[CTRL] [<span class="inline top ui-icon ui-icon-arrowthick-1-n"></span>]</b> : open / close project informations</li>
	<li><b class="ui-state-default">[F]</b> : search</li>
	<li><b class="ui-state-default">[H]</b> : open / close help window of the hovered element</li>
	<li><b class="ui-state-default">[N]</b> : open / close news window</li>
	<li><b class="ui-state-default">[P]</b> : open user preferences</li>
	<li><b class="ui-state-default">[T]</b> : open TAGS manager</li>
	<li><b class="ui-state-default">[O]</b> : open personal NOTES</li>
	<li><b class="ui-state-default">[Z]</b> : back to the last viewed project's tab</li>
	<li><b class="ui-state-default">[R]</b> : open "Root" section of the current project</li>
	<li><b class="ui-state-default">[S]</b> : open "Shots" section of the current project</li>
	<li><b class="ui-state-default">[A]</b> : open "Assets" section of the current project</li>
	<li><b class="ui-state-default">[B]</b> : open departement "Final" of the current project</li>
	<li><b class="ui-state-default">[F1]</b> : open help in a new tab</li>
	<li><b class="ui-state-default">[F4]</b> : back to home</li>
	<li><b class="ui-state-default">[F5]</b> : refresh the whole page</li>
	<li><b class="ui-state-default">[F11]</b> : switch full screen</li>
	<br />
	<b>Supervisors shortcuts:</b>
	<li><b class="ui-state-default">[CTRL] [A]</b> : administration panel</li>
	<li><b class="ui-state-default">[CTRL] [N]</b> : manage news</li>
	<li><b class="ui-state-default">[CTRL] [U]</b> : manage users</li>
	<li><b class="ui-state-default">[CTRL] [P]</b> : manage projects</li>
	<br />
	<b>Developers shortcuts:</b>
	<li><b class="ui-state-default">[C]</b> : open config panel</li>
	<li><b class="ui-state-default">[X]</b> : open debug panel</li>
</ul>

