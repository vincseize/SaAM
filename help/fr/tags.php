			<h4>Types de tags</h4>
			<div class="margeTop10">
				<p>
					Les tags sont très utiles pour marquer des plans, et en faire des listes très précises, afin de trier ou choisir des plans parmi
					la multitude qu'il peut y avoir dans un projet.<br />
					Ils permettent en outre d'exporter des listes aux formats PDF, XML, ou TXT.
				</p>
				Il existe deux types de tags :
				<ul>
					<li>Les tags <b>GLOBAUX</b></li>
					<li>Les tags <b>UTILISATEUR</b></li>
				</ul>
				<p>
					Les tags "globaux" sont définis par l'administrateur du SaAM. Ils seront accessibles dans tout les projets, et visibles par tout le monde.
				</p>
				<p>
					Les tags "utilisateurs" sont aussi utilisables dans tout les projets, mais ils appartiennent à l'utilisateur qui les a créé.
					Seul celui qui crée un tag utilisateur y a accès, mais il peut cependant le partager avec quelqu'un d'autre, afin de créer des listes communes.
				</p>
